void twVGAFont::setColor(twColor col) const{
	col&=0xffffff;
	if(col==ocolor)
		return;
	
	SDL_Surface *surf=fnt->getSurface();
	SDL_Color sc;
	sc.r=twGetR(col); sc.g=twGetG(col); sc.b=twGetB(col);
	sc.unused=0;
    SDL_SetColors(surf, &sc, 1, 1);
	
	ocolor=col;
}

twVGAFont::twVGAFont(){
	
	SDL_Surface *surf;
	surf=SDL_CreateRGBSurfaceFrom(fontdata, 128, 256, 8, 128,
								 0,0,0,0);
	SDL_SetColorKey(surf, SDL_SRCCOLORKEY|SDL_RLEACCEL, 0);
	
	fnt=new twSDLDC(surf, twPoint(0, 0));
	
	ocolor=0xdeadbeef;
}

twVGAFont::~twVGAFont(){
	SDL_FreeSurface(fnt->getSurface());
	delete fnt;
}

void twVGAFont::render(twDC *dc, twColor col, const twPoint& pos, const wstring& str) const{
	twPoint pt=pos;
	size_t n;
	wchar_t br=0;
	
	setColor(col);
	
	for(n=0;n<str.length();n++){
		if(!br){
			if(str[n]==13 || str[n]==10)
				br=str[n];
		}
		if(str[n]==br){
			pt.x=pos.x;
			pt.y+=16;
			continue;
		}else if(str[n]==13 || str[n]==10){
			continue;
		}	
		
		unsigned int id;
		id=str[n];
		if(id>=256)
			id='?';
		
		dc->bitBlt(fnt, pt, twRect((id&0xf)<<3, (id&0xf0), 8, 16));
		
		pt.x+=8;
		
	}
}

twSize twVGAFont::measure(const std::wstring& str) const{
	twPoint pt(0, 16);
	wchar_t n;
	int mw=0;
	wchar_t br=0;
	for(n=0;n<str.length();n++){
		if(!br){
			if(str[n]==13 || str[n]==10)
				br=str[n];
		}
		if(str[n]==br){
			pt.x=0;
			pt.y+=16;
			continue;
		}else if(str[n]==13 || str[n]==10){
			continue;
		}	
		
		pt.x+=8;
		if(pt.x>mw)
			mw=pt.x;
		
	}
	return twSize(mw, pt.y);
}


